from zohocrmsdk.src.com.zoho.api.authenticator import OAuthToken
from zohocrmsdk.src.com.zoho.crm.api import Initializer
from zohocrmsdk.src.com.zoho.crm.api.dc import USDataCenter
from zohocrmsdk.src.com.zoho.crm.api.service_preference import ResponseWrapper, APIException
from zohocrmsdk.src.com.zoho.crm.api.service_preference import ServicePreferenceOperations


class GetServicePreference:
    @staticmethod
    def initialize():
        environment = USDataCenter.PRODUCTION()
        token = OAuthToken(client_id="clientID", client_secret="clientSecret", grant_token="grantToken")
        Initializer.initialize(environment, token)

    @staticmethod
    def get_service_preference():
        service_preference_operations = ServicePreferenceOperations()
        response = service_preference_operations.get_service_preference()
        if response is not None:
            print('Status Code: ' + str(response.get_status_code()))
            if response.get_status_code() in [204, 304]:
                print('No Content' if response.get_status_code() == 204 else 'Not Modified')
                return
            response_object = response.get_object()
            if isinstance(response_object, ResponseWrapper):
                respone_wrapper = response_object
                service_preferences = respone_wrapper.get_service_preferences()
                if service_preferences is not None:
                    print("JobSheetEnabled : " + str(service_preferences.get_job_sheet_enabled()))
            elif isinstance(response_object, APIException):
                print("Status: " + response_object.get_status().get_value())
                print("Code: " + response_object.get_code().get_value())
                print("Details")
                details = response_object.get_details()
                for key, value in details.items():
                    print(key + ' : ' + str(value))
                print("Message: " + response_object.get_message())


GetServicePreference.initialize()
GetServicePreference.get_service_preference()